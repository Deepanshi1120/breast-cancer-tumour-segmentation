class DenseNetWithCBAM(nn.Module):
    """
    Insert CBAM after the output of the DenseNet121 feature extractor,
    then connect it to a custom classification head.
    """
    def __init__(self, num_classes=2, dropout_rate=0.4):
        super().__init__()

        # DenseNet121 backbone
        backbone = DenseNet121(spatial_dims=2, in_channels=3, out_channels=1024)
        self.features = backbone.features

        # CBAM
        self.cbam = CBAM(in_channels=1024, reduction=16, kernel_size=7)

        # classifier
        self.classifier = nn.Sequential(
            nn.ReLU(inplace=True),
            nn.AdaptiveAvgPool2d(1),
            nn.Flatten(),
            nn.Dropout(p=dropout_rate),
            nn.Linear(1024, 256),
            nn.ReLU(inplace=True),
            nn.Dropout(p=dropout_rate / 2),
            nn.Linear(256, num_classes)
        )

    def forward(self, x):
        feat = self.features(x)   # (B, 1024, H', W')
        feat = self.cbam(feat)    # CBAM attention
        out  = self.classifier(feat)
        return out


