#. Training Cycle
# -------------------------------------------------------
train_losses, val_accs, val_losses, lr_history = [], [], [], []
best_val_acc   = 0.0
patience_count = 0

for epoch in range(MAX_EPOCHS):
    # ---------- Training  ----------
    model.train()
    epoch_loss, steps = 0.0, 0
    loop = tqdm(train_loader, desc=f"Epoch [{epoch+1}/{MAX_EPOCHS}]")

    for imgs, labels in loop:
        imgs, labels = imgs.to(device), labels.to(device)
        optimizer.zero_grad()
        outputs = model(imgs)
        loss    = loss_fn(outputs, labels)
        loss.backward()
        optimizer.step()
        epoch_loss += loss.item()
        steps += 1
        loop.set_postfix(avg_loss=epoch_loss / steps)

    avg_train_loss = epoch_loss / steps
    train_losses.append(avg_train_loss)
    lr_history.append(optimizer.param_groups[0]['lr'])
    scheduler.step()

    # ---------- Validation----------
    model.eval()
    correct, total, val_loss_sum = 0, 0, 0.0

    with torch.no_grad():
        for imgs, labels in valid_loader:
            imgs, labels = imgs.to(device), labels.to(device)
            outputs = model(imgs)
            val_loss_sum += loss_fn(outputs, labels).item()
            preds    = torch.argmax(outputs, dim=1)
            correct += (preds == labels).sum().item()
            total   += len(labels)

    val_acc     = correct / total
    avg_val_loss = val_loss_sum / len(valid_loader)
    val_accs.append(val_acc)
    val_losses.append(avg_val_loss)

    print(
        f"Epoch [{epoch+1:02d}/{MAX_EPOCHS}] "
        f"Train Loss: {avg_train_loss:.4f} | "
        f"Val Loss: {avg_val_loss:.4f} | "
        f"Val Acc: {val_acc:.4f} | "
        f"LR: {lr_history[-1]:.2e}"
    )

    # ---------- Save Best ----------
    if val_acc > best_val_acc:
        best_val_acc = val_acc
        torch.save(model.state_dict(), model_path)
        print(f"  ✅ Best model saved (Val Acc: {best_val_acc:.4f})")
        patience_count = 0
    else:
        patience_count += 1

    # ----------Early stop ----------
    if patience_count >= PATIENCE:
        print(f"\n⏹ Early stopping at epoch {epoch+1}")
        break

print(f"\n🏆 Best Validation Accuracy: {best_val_acc:.4f}")

